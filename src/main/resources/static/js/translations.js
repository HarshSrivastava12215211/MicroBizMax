/* ════════════════════════════════════════
   Core translation engine
════════════════════════════════════════ */
function applyTranslations(lang) {
  const t = translations[lang] || translations['en'];

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (!t[key]) return;

    if (el.tagName === 'INPUT' && el.type === 'button') {
      el.value = t[key];
    } else if (el.tagName === 'INPUT' && el.placeholder) {
      el.placeholder = t[key];
    } else {
      el.innerHTML = t[key];
    }
  });

  // Update <html lang> attribute for accessibility
  document.documentElement.lang = lang;
  localStorage.setItem('site_lang', lang);
}

/* ════════════════════════════════════════
   Auto-init on page load
════════════════════════════════════════ */
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('site_lang') || 'en';

  // Sync all lang selectors on the page
  document.querySelectorAll('.lang-selector, #langSelect').forEach(select => {
    select.value = savedLang;
    select.addEventListener('change', e => applyTranslations(e.target.value));
  });

  applyTranslations(savedLang);
});
